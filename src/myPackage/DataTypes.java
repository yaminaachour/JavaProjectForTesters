package myPackage;

public class DataTypes {

	public static void main(String[] args) {

		//Primitive
		int a = 10;
		double d = 20.5; 
		char x = 'A';
		boolean b = false;
		
		
		//Derived 
		String s = "Hello";
		
		System.out.println(a);
		System.out.println(d);
		System.out.println(x);
		System.out.println(b);
		System.out.println(s);

	}

}
